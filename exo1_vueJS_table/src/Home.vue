      </md-toolbar>
        <md-field>
        <label>Search</label>

      </md-field>
    </div>

        <h1>Liste des restaurants : {{restaurants.length}}  </h1>


          <div>
            <md-table>
              <md-table-row>
                <md-table-head>Name</md-table-head>
                <md-table-head>Cuisine</md-table-head>
                <md-table-head>Actions</md-table-head>
              </md-table-row>
              <restaurant v-for="r,index in restaurants"
               v-bind:restaurant="r"
               v-bind:style="{background:getColor(index)}"
               v-bind:class="[index == 4 ? 'bordureRouge' : '']" @modifRestaurant="modifierRestaurant" @change-selected-values="updateAddForm" @getDataFromServer='getDataFromServer' > </restaurant>
            </md-table>
          </div>
      <div style="text-align: center;">
              <button v-on:click="firstPage" ><<</button>
              <button v-on:click="pagePrecedente" v-if="page > 1 "><</button>
              <span v-if="nbrPages > 6" >
                <span v-if="page > 3" > ... </span>
              </span>
              <a href="#"  v-on:click="getPage(n)" v-for="n in [page,page + 1 ,page + 2]">{{n}},</a>
              <span v-if="nbrPages > 6" >
                <span v-if="page < nbrPages - 3 " > ... </span>
              </span>
              <button v-on:click="pageSuivante" v-if="nbrPages > page ">></button>
              <button v-on:click="lastPage">>></button>
        </div>
        </div>
</div>
</template>

<script>
import restaurant from './restaurant.vue';
import ajoutrestaurant from './ajoutrestaurant.vue'

export default {
  name: 'home',
  data : function(){ return {
          restaurants : [],
          name : "", // I defined this attribut here to be acceessible from all components using $root
          cuisine : "", // the same thing as name
          nameSearch: "",
          counter:0,
          page : 1,
          pagesize : 20,
          nbrPages : 0
          }

  },
  components: {
    restaurant,
    ajoutrestaurant
  },
  mounted() {
          console.log("loading data from server");
          this.getDataFromServer(false);
          this.getDataFromServer(true);
        },
        methods: {
        
        modifierRestaurant: function (id) {
            let url = "http://localhost:8080/api/restaurants/"+id;

            let that = this;
            fetch(url, {
              headers: {
                  'Accept': 'application/json',
                  'Content-Type': 'application/json'
                },
                method: "PUT",
                body: JSON.stringify({nom : that.name,cuisine: that.cuisine})
            })
            .then(function(responseJSON) {
                responseJSON.json()
                    .then(function(res) {
                        // Maintenant res est un vrai objet JavaScript
                        that.getDataFromServer(false)
                        console.log("data send successfuly")
                    });
                })
                .catch(function (err) {
                    console.log(err);
            });

            // on remet à zéro les champs
            this.name = ''
            this.cuisine= ''


        },

          // if all is true it will simply initialise the number of pages
          getDataFromServer(all){
            let url = "http://localhost:8080/api/restaurants";
            if (!all) {
              url += "?page=" + this.page + "&pagesize=" + this.pagesize;
            } else {
              url += "?page=1&pagesize="+Number.MAX_VALUE;

            }


          let that = this;
          fetch(url)
            .then(function(responseJSON) {
                responseJSON.json()
                .then(function(res) {
                  if (!all) {
                    that.restaurants = [];
                    that.restaurants= res.data ;
                  }
                  if (all) {
                    that.nbrPages = parseInt(res.count/that.pagesize);
                  }
                });
              })
              .catch(function (err) {
                console.log(err);
              });
            },


            updateAddForm : function(rest){
              this.name = rest.name
              this.cuisine = rest.cuisine
            }
            ,
            pageSuivante: function () {
              if (this.nbrPages > this.page) {
                this.page++;
                this.getDataFromServer(false);
              }

            },
            firstPage: function () {

                this.page = 1;
                this.getDataFromServer(false);


            },
            lastPage: function () {

                this.page = this.nbrPages;
                this.getDataFromServer(false);


            },
            pagePrecedente: function () {
              if (this.page > 1) {
                this.page--;
                this.getDataFromServer(false);
              }
            },
            getPage : function(n) {
              if (this.nbrPages >= n && n > 0) {
                this.page=n;
                this.getDataFromServer(false);
              }
            },
            getColor: function(index) {
              if (index%2==0) return "#f2f2f2";
              return "white";
            }
        }

}
</script>


